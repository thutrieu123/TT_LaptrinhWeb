	//Phuong thuc che dau email
	public String decodeEmail(String email) {
		String result = "";
		String[] split = email.split("@");
		for(int i = 0;i < 4;i++) {
			result += split[0].charAt(i);
		}
		result += "************@"+split[1];
		return result;
	}
	

	//Phuong thuc ma hoa theo SHA-1
	
	public static String encrypt(String str) {
		String salt = "adABDK*@asjdkasfl;sada";
		String result = null;
		str += str + salt;
		
		try {
			byte[] dataBytes = str.getBytes("UTF-8");
			MessageDigest md = MessageDigest.getInstance("SHA-1");
			result = Base64.encodeBase64String(md.digest(dataBytes));
			
			
		}catch(Exception e) {
			e.printStackTrace();
		}
		return result;
	}
	//Phuong thuc chuyen doi ki tu co dau sang k dau
	public static char removeAccent(char ch) {
        int index = Arrays.binarySearch(SOURCE_CHARACTERS, ch);
        if (index >= 0) {
            ch = DESTINATION_CHARACTERS[index];
        }
        return ch;
    }
	//Phuong thuc chuyen doi chuoi co dau sang khong dau
    public static String convertUTF_8(String str) {
        StringBuilder sb = new StringBuilder(str);
        for (int i = 0; i < sb.length(); i++) {
           if(str.charAt(i) == ' ') {
        	   sb.setCharAt(i, '-');
           }else {
        	   sb.setCharAt(i, removeAccent(sb.charAt(i)));
           }
        }
        return sb.toString();
    }
